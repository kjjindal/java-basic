public class switchcase {

    public static void main(String[] args) {
        int age = 18;

        switch (age) {
            case 18:
                System.out.print("age is 18");

                break;

            case 20:
                System.out.println("age is 20");

            case 60:
                System.out.println("age is 60");

            default:
                break;
        }
    }

}
